    private void btpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesquisarActionPerformed

        int cod = Integer.parseInt(JOptionPane.showInputDialog(this, "Digite o código"));

        try {
            Cidade cidade = cidadeDAO.pesquisarCidade(cod);

            if (cidade != null) {
                mostrarDados(cidade);
                JOptionPane.showMessageDialog(this, "Cidade encontrado...");

            } else {
                JOptionPane.showMessageDialog(this, "A cidade não foi encontrado...");

            }

        } catch (SQLException ex) {
            Logger.getLogger(CadCidade.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btpesquisarActionPerformed

