    private void iniciarBD() {

        try {

            cidadeDAO = new CidadeDAO();
            
            primeiro();
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }
    }

   
    //Atualizar as informações dos campos de texto.
    public void mostrarDados(Cidade cidade) {

       

            tfcodigo.setText(String.valueOf(cidade.getCod()));
            tfCidade.setText(cidade.getNomeCidade());
            tfEstado.setText(cidade.getNomeEstado());

      
    }

    public void primeiro() {

        Cidade cidade;

        try {
            cidade = cidadeDAO.primeiro();

            if (cidade != null) {
                mostrarDados(cidade);

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }

    }

    public void anterior() {

        Cidade cidade;

        try {
            cidade = cidadeDAO.anterior();

            if (cidade != null) {
                mostrarDados(cidade);

            } else {
                JOptionPane.showMessageDialog(this, "Primeiro item selecionado");

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }

    }

    public void proximo() {

        Cidade cidade;

        try {
            cidade = cidadeDAO.proximo();

            if (cidade != null) {
                mostrarDados(cidade);

            } else {
                JOptionPane.showMessageDialog(this, "Ultimo item selecionado");

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }

    }

    public void ultimo() {

        Cidade cidade;

        try {
            cidade = cidadeDAO.ultimo();

            if (cidade != null) {
                mostrarDados(cidade);

            }
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());

        }

    }

    private void limparCampos() {
   
    //Apaga as informações dos campos de texto da interface gráfica.
        tfcodigo.setText("");
        tfCidade.setText("");
        tfEstado.setText("");

        tfcodigo.requestFocus();
    }

