    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed

        try {
            int cod = Integer.parseInt(tfcodigo.getText());
            String nomeCidade = tfCidade.getText();
            String nomeEstado = tfEstado.getText();
            
            
            Cidade cidade = new Cidade();

            cidade.setCod(cod);
            cidade.setNomeCidade(nomeCidade);
            cidade.setNomeEstado(nomeEstado);
            
            cidadeDAO.salvarRegistro(cidade);

            JOptionPane.showMessageDialog(this, "Cidade Salva com sucesso");

        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }finally{
            limparCampos();
        }


    }//GEN-LAST:event_btsalvarActionPerformed

